 class Restaurant{

     name: string;
     address: string;
     phone: string;
     nip: string;
     email: string;
     www: string;

     constructor(name: string, address: string, phone: string, nip: string, email: string, www: string)
     {
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.nip = nip;
        this.email = email;
        this.www = www;
     }
 }

 export default Restaurant;